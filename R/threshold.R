thresholds = function(x, y, xthr, ythr){
  xn    <- .pradaMakeX(x, y)
  if(!is.numeric(xthr) | !is.numeric(ythr))
    stop("'xthr' and 'ythr' must be numeric vectors")
 
  bx  <- (xn[,1]<=xthr)
  by  <- (xn[,2]<=ythr)
  nbx <- !bx
  nby <- !by
  return(rbind(c(sum( bx& by), sum(nbx& by)),
               c(sum( bx&nby), sum(nbx&nby))))
}

 
